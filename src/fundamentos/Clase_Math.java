package fundamentos;

public class Clase_Math {
    public static void main(String[] args) {
        System.out.println(Math.pow(2,5));
        System.out.println(Math.ceil(456.98));
        System.out.println(Math.floor(456.98));
        System.out.println(Math.pow(8, (double)1/3));
        System.out.println(Math.abs(-5));
        System.out.println(Math.random()*(200-180+1)+180);
    }
}
